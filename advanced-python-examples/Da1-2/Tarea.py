# Operaciones matemáticas
# Declaramos dos números
num1 = 15
num2 = 7

# Suma
suma = num1 + num2
print("La suma de", num1, "y", num2, "es:", suma)

# Resta
resta = num1 - num2
print("La resta de", num1, "y", num2, "es:", resta)

# Multiplicación
multiplicacion = num1 * num2
print("La multiplicación de", num1, "y", num2, "es:", multiplicacion)

# División
division = num1 / num2
print("La división de", num1, "y", num2, "es:", division)

# Manipulación de cadenas
nombre = "Manuel"
apellido = "Duarte"

# Concatenación de cadenas
nombre_completo = nombre + " " + apellido
print("El nombre completo es:", nombre_completo)

# Conversión a mayúsculas
nombre_mayusculas = nombre_completo.upper()
print("Nombre completo en mayúsculas:", nombre_mayusculas)

# Conversión a minúsculas
nombre_minusculas = nombre_completo.lower()
print("Nombre completo en minúsculas:", nombre_minusculas)

# Contar el número de caracteres
numero_caracteres = len(nombre_completo)
print("El número de caracteres en el nombre completo es:", numero_caracteres)