#include <iostream>

using namespace std;

int main()
{

  int integer = 20;        // Type: integer
  float floatante = 20.55; // Type: float
  double doublee = 20.556; // Type: double
  char letra = 'a';

  cout << "Integer: " << integer << endl;
  cout << "Float: " << floatante << endl;
  cout << "Double: " << doublee << endl;
  cout << "Char: " << letra << endl;

  return 0;
}
