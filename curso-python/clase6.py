# Operadores numericos


a = 10
b = 3

print(a + b)  # 13
print(a - b)  # 7
print(a * b)  # 30
print(a / b)  # 3.3333333333333335

# Division entera
print(a // b)  # 3

# Modulo
print(a % b)  # 1

# Potencia
print(a**b)  # 1000

# Operadores de comparación
print(a == b)  # False
print(a != b)  # True
print(a > b)  # True
print(a < b)  # False
print(a >= b)  # True
print(a <= b)  # False

# Operadores lógicos
print(True and False)  # False
print(True or False)  # True
print(not True)  # False


# PENDAS
opration = 10 + 3 * 2  #  10 + 6 = 16
print(opration)  # 16

opration = (10 + 3) * 2  # 13 * 2 = 26
print(opration)  # 26

opration = 10 + 3 * 2**2  # 10 + 3 * 4 = 10 + 12 = 22
print(opration)  # 22

opration = (10 + 3) * 2**2  # 13 * 4 = 52
print(opration)  # 52
