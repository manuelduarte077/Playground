a = [1, 2, 3, 4, 5]

b = a  # b = [1, 2, 3, 4, 5]

print(a)  # [1, 2, 3, 4, 5]
print(b)  # [1, 2, 3, 4, 5]
del a[0]  # a = [2, 3, 4, 5]
print(id(a))  # [2, 3, 4, 5]
print(id(b))  # [2, 3, 4, 5]

c = a[:]  # c = [2, 3, 4, 5]
print(id(a))  # [2, 3, 4, 5]
print(id(b))  # [2, 3, 4, 5]
print(id(c))  # [2, 3, 4, 5]

a.append(6)  # a = [2, 3, 4, 5, 6]
print(a)  # [2, 3, 4, 5, 6]
print(b)  # [2, 3, 4, 5, 6]
print(c)  # [2, 3, 4, 5]
